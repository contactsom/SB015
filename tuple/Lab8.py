print(countryTuple)
print(type(countryTuple)) # <class 'tuple'>

print("------------------------------")
countryTuple1 = ("",)
print(countryTuple1)
print(type(countryTuple1)) # <class 'tuple'>

print("------------------------------")
countryTuple2 = ()
print(countryTuple2)
print(type(countryTuple2)) # <class 'tuple'>

print("------------------------------")
countryTuple3 = tuple()
print(countryTuple3)
print(type(countryTuple3)) # <class 'tuple'>


print("START- This is Example for Accessing Elements of Tuple - By Using Index")

#   0,   1,   2,   3,    4,   +ve Index Representation
#   10,  20 , 30 , 40 , 50
#  -5,   -4,  -3,  -2,  -1    -ve Index Representation

print("------------------------------")

mytuple=(10,  20 , 30 , 40 , 50)
print(mytuple)
print(type(mytuple))

print("---- +ve Index Representation ---- ")
print(mytuple[0])
print(mytuple[1])
print(mytuple[2])
print(mytuple[3])
print(mytuple[4])

print("---- -ve Index Representation ---- ")
print(mytuple[-5])
print(mytuple[-4])
print(mytuple[-3])
print(mytuple[-2])
print(mytuple[-1])








print("END- This is Example for Tuple ")
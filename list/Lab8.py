print("START- This is Example for List Slicing ")

# -Ve  =   -5,  -4  -3, -2, -1
# List =  [ 10, 20, 30, 40, 50 ]
# +Ve  =    0,  1,   2,  3,  4

print("------------------------------------------------------")

list =   [ 10, 20, 30, 40, 50 ]

print("[1:3]",list[1:3]) # 20, 30

print("[-4:-2]",list[-4:-2]) # 20, 30


